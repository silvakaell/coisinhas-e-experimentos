#com esse script você pode calcular o tempo perdido pelo kurapika do hunter x hunter quando ele usa o emperor time!


s = float(input('Olá, Kurapika! Digite quantos segundos você usou o Emperor Time aqui: '))
hp = s * 1 #hora perdida
dp = hp/24 #dias perdidos
ap = dp/365 # anos perdidos
print('Kurapika usou o Emperor Time por {} segundos, ele perdeu {} horas de vida, {} dias, e {} anos'.format(s, hp, dp, ap))














